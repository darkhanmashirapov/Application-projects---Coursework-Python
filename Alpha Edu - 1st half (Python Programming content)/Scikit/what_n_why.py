# Что такое классификация?
"""
Классификация - это один из ключевых видов задач машинного
обучения. Её цель - разделить данные на определённые категории
(классы).

Пример: вы хотите создать систему, которая может определить,
является ли сообщение электронной почты спамом или нет.
Данные (тексты писем) передаются модели, которая обучается
на размеченных примерах ("спам" или "не спам"), а затем
используется для классификации новых сообщений.

## Как классификация работает?
1. **Сбор данных**:
   - Вам нужны размеченные данные, где указано, к какому классу
     относятся записи.
2. **Предобработка данных**:
   - Удаление пропусков, нормализация данных, перевод текста
     в числовой формат (например, TF-IDF для текстов).
3. **Выбор алгоритма**:
   - Подходящий алгоритм выбирается в зависимости от задачи
     и характеристик данных.
4. **Обучение модели**:
   - Алгоритм обучается на тренировочных данных.
5. **Тестирование**:
   - Модель проверяется на тестовой выборке для оценки качества.
6. **Прогнозирование**:
   - Использование модели для классификации новых данных.
"""

# Основные алгоритмы классификации
"""
1. **Логистическая регрессия**:
   - Математический метод, который предсказывает вероятность
     принадлежности объекта к определённому классу.
   - Прост в понимании и часто используется для бинарной
     классификации (например, "да" или "нет").

2. **Метод опорных векторов (SVM)**:
   - Этот алгоритм строит гиперплоскость, которая разделяет
     классы в пространстве признаков. Подходит для сложных
     наборов данных с многомерными признаками.

3. **Решающие деревья**:
   - Построение дерева, где каждый узел представляет вопрос
     о данных, а листья - предсказанные классы. Прост в
     интерпретации и визуализации.

4. **Случайный лес**:
   - Ансамбль решающих деревьев, который улучшает точность
     классификации за счёт объединения их прогнозов.

5. **Наивный байесовский классификатор**:
   - Базируется на теореме Байеса. Часто используется для
     анализа текстов, так как хорошо работает с частотными
     данными (например, спам-фильтры).

6. **k-ближайших соседей (k-NN)**:
   - Объект классифицируется на основе большинства классов его
     ближайших соседей в пространстве признаков.

7. **Нейронные сети**:
   - Мощный инструмент для работы с большими и сложными данными.
     Используется для задач, таких как распознавание изображений.

Для простого понимания алгоритмы можно представить как разные
способы разделения данных на группы с учётом их особенностей.
"""

